//to perform data types
#include<stdio.h>
int main()
{
	int a;
	float b;
	char c;
	double z;
	printf("enter a value");
	scanf("%d",&a);
	printf("integer value is:%d",a);
	printf("enter b value");
	scanf("%f",&b);
	printf("float value is:%f",b);
	printf("enter c value");
	scanf("%c",&c);
	printf("char value is:%c",c);
	printf("enter z value");
	scanf("%lf",&z);
	printf("double value is:%lf",z);
	return 0;
}


//to check whether the given number is positive or negative in if else statement
#include<stdio.h>
int main()
{
	int n;
	printf("enter the n value");
	scanf("%d", &n);
	if(n>=0)
	{
		printf("the n value is positive");
	}
	else
	{
		printf("the n value is negative");
	}
	return 0;
}
